package com.example.spring.attendance.entity;

public class JsonData {
	String divide;
	String code;
	String title;
	String AnnualLeaveReflection;
	String UseOrFailure;
	String note;
	
	public String getDivide() {
		return divide;
	}
	public void setDivide(String divide) {
		this.divide = divide;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getAnnualLeaveReflection() {
		return AnnualLeaveReflection;
	}
	public void setAnnualLeaveReflection(String annualLeaveReflection) {
		AnnualLeaveReflection = annualLeaveReflection;
	}
	public String getUseOrFailure() {
		return UseOrFailure;
	}
	public void setUseOrFailure(String useOrFailure) {
		UseOrFailure = useOrFailure;
	}
	public String getNote() {
		return note;
	}
	public void setNote(String note) {
		this.note = note;
	}
}
